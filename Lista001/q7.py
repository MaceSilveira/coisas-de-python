import math

angulo_graus=80
seno = math.sin(angulo_graus)
cosseno = math.cos(angulo_graus)
tangente = math.tan(angulo_graus)
cossecante = 1/seno
secante = 1/cosseno
cotangente = 1/tangente

print ("O seno, cosseno, tangente, secante, cossecante, e cotangente de 80° respectivamente é", seno, cosseno, tangente, secante, cossecante, cotangente)